#include<stdio.h>
#include<math.h>

int main() {
    
    printf("Ceiling function : %f\n", ceil(3.6));
    printf("Ceiling function : %f\n", ceil(3.3));
    printf("floor function : %f\n", floor(3.6));
    printf("floor function : %f\n", floor(3.3));
    printf("Square function : %f\n", sqrt(16));
    printf("Power function : %f\n", pow(2,3));
    printf("absolute function : %d\n", abs(-12));

    return 0;
    
}